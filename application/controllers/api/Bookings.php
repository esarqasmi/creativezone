class Bookings extends REST_Controller {

    function __construct()
    {
        header('Access-Control-Allow-Origin: *');
        header("Access-Control-Allow-Methods: GET, POST, OPTIONS, PUT, DELETE");
        parent::__construct();
        $this->load->model('booking_model');
        $this->load->library('form_validation');
    }

  


    public function bookings_get(){

       
        $bookings = $this->booking_model->get_all_bookings();
       
        if($bookings){
            $this->set_response($bookings, REST_Controller::HTTP_CREATED);
        }else{
            $this->response(false, REST_Controller::HTTP_BAD_REQUEST);
        }
    }
}
